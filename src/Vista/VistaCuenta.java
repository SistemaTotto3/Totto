    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String strIdOrden = textidOrden.getText().trim();
        String strIdCliente = textidCliente.getText().trim();
        String strTotalCuenta = texttotal_cuenta.getText().trim();
        String estado_cuenta = (String) comboestado_cuenta.getSelectedItem();

        if (!strIdOrden.isEmpty() && !strIdCliente.isEmpty() && !strTotalCuenta.isEmpty() && !estado_cuenta.isEmpty()) {
            try {
                int idOrden = Integer.parseInt(strIdOrden);
                int idCliente = Integer.parseInt(strIdCliente);
                float total_cuenta = Float.parseFloat(strTotalCuenta);
                Date fecha = selectorfecha_cuenta.getDate();
                java.sql.Date fecha_cuenta = new java.sql.Date(fecha.getTime());

                this.cuentaControlador.crearCuenta(idOrden, idCliente, fecha_cuenta, total_cuenta, estado_cuenta);
                cargarDatosTabla();
                limpiar();
            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, llene todos los campos.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = tablacuenta.getSelectedRow();
        if (filaSeleccionada != -1) {
            int id_cuenta = (int) tablacuenta.getValueAt(filaSeleccionada, 0);
            cuentaControlador.eliminarCuenta(id_cuenta);
            cargarDatosTabla();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecciona una fila para eliminar.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

